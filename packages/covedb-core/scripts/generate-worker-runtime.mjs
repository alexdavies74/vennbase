import { createHash } from "node:crypto";
import { mkdir, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

import { build } from "esbuild";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const packageDir = resolve(scriptDir, "..");

const entryPoint = resolve(packageDir, "src/worker/deploy-runtime.ts");
const outputDir = resolve(packageDir, "src/worker/dist");
const outputPath = resolve(outputDir, "generated-runtime.ts");

// Workflow reminder: regenerate generated-runtime.ts whenever deploy-runtime.ts or its transitive
// worker code changes. Provisioning derives a version id from the generated bytes.

const buildResult = await build({
  entryPoints: [entryPoint],
  bundle: true,
  write: false,
  format: "iife",
  platform: "browser",
  target: ["es2022"],
  treeShaking: true,
  legalComments: "none",
});

if (buildResult.outputFiles.length !== 1) {
  throw new Error(`Expected one output file, got ${buildResult.outputFiles.length}`);
}

const bundledSource = buildResult.outputFiles[0].text.trim();
const runtimeId = createHash("sha256").update(bundledSource).digest("hex").slice(0, 12);
const escaped = bundledSource
  .replace(/\\/g, "\\\\")
  .replace(/`/g, "\\`")
  .replace(/\$\{/g, "\\${");

const generated = `// Auto-generated by scripts/generate-worker-runtime.mjs. Do not edit manually.\nexport const CLASSIC_WORKER_RUNTIME_ID = "${runtimeId}";\nexport const CLASSIC_WORKER_RUNTIME = \`\n${escaped}\n\`;\n`;

await mkdir(outputDir, { recursive: true });
await writeFile(outputPath, generated, "utf8");
console.log(`Generated ${outputPath}`);
